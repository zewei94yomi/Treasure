// ============ 大逃亡长图：程序生成（每局随机的房间链，左出生右撤离） ============
MAPS.escape = { name: '长夜奔逃', theme: 'manor', gen: true, noScale: true, mods: {} };
function genEscapeAscii() {
  // 先排房间再定宽度：13~15 间房从西到东，走廊相连（构造即连通）
  const H = 40;
  const rooms = [];
  let cx = 3;
  const n = 13 + Math.floor(Math.random() * 3);
  for (let i = 0; i < n; i++) {
    const rw = 18 + Math.floor(Math.random() * 12);
    const rh = 16 + Math.floor(Math.random() * 16);
    const ry = 2 + Math.floor(Math.random() * (H - rh - 4));
    rooms.push({ x: cx, y: ry, w: rw, h: rh, cx: cx + (rw >> 1), cy: ry + (rh >> 1) });
    cx += rw + 5 + Math.floor(Math.random() * 6);
  }
  const W = cx + 3;
  const g = Array.from({ length: H }, () => Array(W).fill('#'));
  const carve = (x0, y0, w, h) => {
    for (let y = Math.max(1, y0); y < Math.min(H - 1, y0 + h); y++)
      for (let x = Math.max(1, x0); x < Math.min(W - 1, x0 + w); x++) g[y][x] = '.';
  };
  for (const r of rooms) carve(r.x, r.y, r.w, r.h);
  for (let i = 1; i < rooms.length; i++) {         // L 形走廊（宽 4）
    const a = rooms[i - 1], b = rooms[i];
    carve(a.cx, a.cy - 2, b.cx - a.cx + 2, 4);
    carve(b.cx - 2, Math.min(a.cy, b.cy) - 2, 4, Math.abs(b.cy - a.cy) + 4);
  }
  const put = (r, ch, k) => {
    for (let i = 0; i < k; i++) {
      const x = r.x + 2 + Math.floor(Math.random() * (r.w - 4));
      const y = r.y + 2 + Math.floor(Math.random() * (r.h - 4));
      if (g[y] && g[y][x] === '.') g[y][x] = ch;
    }
  };
  rooms.forEach((r, i) => {
    if (i === 0) return;                            // 出生房留干净
    put(r, '*', 2 + Math.floor(Math.random() * 3));
    put(r, 'm', i < 3 ? 2 : 3);
    put(r, 'c', 3);
    put(r, 'g', 2);
    put(r, 't', 2);
  });
  MAPS.escape.rooms = rooms;                        // 导出房间矩形（怪潮触发用）
  const r0 = rooms[0], rl = rooms[rooms.length - 1];
  g[r0.cy][r0.x + 2] = '1';
  g[Math.min(H - 3, r0.cy + 2)][r0.x + 2] = '7';
  const rm = rooms[Math.floor(rooms.length / 2)];
  g[rm.cy][rm.cx] = 'S';
  for (let y = rl.cy - 1; y <= rl.cy + 1; y++)      // 撤离区 3×4
    for (let x = rl.x + rl.w - 6; x < rl.x + rl.w - 2; x++)
      if (g[y] && g[y][x]) g[y][x] = 'X';
  return g.map(r => r.join(''));
}

let MapData = null;   // 当前对局的地图数据，loadMap() 填充

// 第九轮：所有地图加载时放大（每格 → 2×2，面积 ×4）
// 唯一点位（出生1/7、商人S）只留左上角；密度点位（宝箱c/金g/怪m/火把t）留左上+右下两份；
// 结构字符（墙/障碍/地板/装饰/撤离区X）原样铺满 2×2。
function scaleAscii(rows) {
  const out = [];
  for (const row of rows) {
    let a = '', b = '';
    for (const ch of row) {
      if (ch === '1' || ch === '7' || ch === 'S') { a += ch + '.'; b += '..'; }
      else if (ch === 'c' || ch === 'g' || ch === 'm' || ch === 't') { a += ch + '.'; b += '.' + ch; }
      else { a += ch + ch; b += ch + ch; }
    }
    out.push(a, b);
  }
  return out;
}

function loadMap(mapId) {
  const def = MAPS[mapId] || MAPS.manor;
  if (def.gen) def.ascii = genEscapeAscii();        // 大逃亡：每局重新生成
  const ascii = def.noScale ? def.ascii : scaleAscii(def.ascii);
  const h = ascii.length, w = ascii[0].length;
  const solid = [], obstacles = [], decorTiles = [], torches = [];
  const chestSpots = [], goldSpots = [], monsterNodes = [], spawns = [null, null];
  const exitTiles = [];
  const decorGrid = [];        // '~' 装饰格（冰面/水面等，机制由 mods 决定）
  let merchantSpot = null;     // 'S' 神秘商人点位
  for (let y = 0; y < h; y++) {
    solid.push([]); decorGrid.push([]);
    for (let x = 0; x < w; x++) {
      const ch = ascii[y][x];
      solid[y].push(ch === '#' || ch === '*');
      decorGrid[y].push(ch === '~');
      const cx = x * TILE + TILE / 2, cy = y * TILE + TILE / 2;
      if (ch === '*') obstacles.push({ tx: x, ty: y });
      else if (ch === '~') decorTiles.push({ tx: x, ty: y });
      else if (ch === 't') torches.push({ x: cx, y: cy });
      else if (ch === 'c') chestSpots.push({ x: cx, y: cy, tx: x, ty: y });
      else if (ch === 'g') goldSpots.push({ x: cx, y: cy, tx: x, ty: y });
      else if (ch === 'm') monsterNodes.push({ x: cx, y: cy, tx: x, ty: y });
      else if (ch === 'S') merchantSpot = { x: cx, y: cy };
      else if (ch === '1') spawns[0] = { x: cx, y: cy };
      else if (ch === '7') spawns[1] = { x: cx, y: cy };
      else if (ch === 'X') exitTiles.push({ tx: x, ty: y });
    }
  }
  const ex0 = Math.min(...exitTiles.map(t => t.tx)), ex1 = Math.max(...exitTiles.map(t => t.tx));
  const ey0 = Math.min(...exitTiles.map(t => t.ty)), ey1 = Math.max(...exitTiles.map(t => t.ty));
  const exitRect = { x: ex0 * TILE, y: ey0 * TILE, w: (ex1 - ex0 + 1) * TILE, h: (ey1 - ey0 + 1) * TILE };

  // 特殊点位(c/g/m/t/S)打在冰面/水面上会在装饰网格上留洞：按 8 邻域多数补回
  const snapshot = decorGrid.map(row => row.slice());
  for (let y = 1; y < h - 1; y++) for (let x = 1; x < w - 1; x++) {
    if (solid[y][x] || snapshot[y][x]) continue;
    let n = 0;
    for (let dy = -1; dy <= 1; dy++) for (let dx = -1; dx <= 1; dx++) {
      if ((dy || dx) && snapshot[y + dy][x + dx]) n++;
    }
    if (n >= 5) decorGrid[y][x] = true;
  }

  // 地图没画 'S' 时：取离出生点第二远的怪物节点旁作为商人摊位
  if (!merchantSpot && monsterNodes.length && spawns[0]) {
    const far = monsterNodes.slice().sort((a, b) =>
      Math.hypot(b.x - spawns[0].x, b.y - spawns[0].y) - Math.hypot(a.x - spawns[0].x, a.y - spawns[0].y))[1] || monsterNodes[0];
    merchantSpot = { x: far.x + TILE, y: far.y };
  }

  const themeKey = typeof def.theme === 'string' ? def.theme : def.id;
  const themeObj = typeof def.theme === 'string' ? (MAPS[def.theme] ? MAPS[def.theme].theme : {}) : def.theme;
  MapData = { def, ascii, escapeRooms: def.rooms || null, theme: themeObj, themeKey, mods: def.mods || {}, w, h, solid, obstacles, decorTiles, decorGrid, torches,
              chestSpots, goldSpots, monsterNodes, spawns, exitTiles, exitRect, merchantSpot,
              pxW: w * TILE, pxH: h * TILE };

  // 庄园老图没画火把字符，按规则自动布置在贴墙的地板上
  if (def.autoTorches) {
    for (let y = 2; y < h - 2; y++) for (let x = 2; x < w - 2; x++) {
      if (solid[y][x]) continue;
      if ((x * 7 + y * 11) % 37 !== 0) continue;
      if (!solid[y - 1][x] && !solid[y][x - 1] && !solid[y][x + 1]) continue;
      torches.push({ x: x * TILE + TILE / 2, y: y * TILE + TILE / 2 });
    }
  }

  // 小地图底图：按地图尺寸自适应缩放，封顶 300×150（大图不再霸屏）
  const mm = document.createElement('canvas');
  const S = Math.min(4, 300 / w, 150 / h);
  mm.width = w * S; mm.height = h * S;
  const mctx = mm.getContext('2d');
  mctx.fillStyle = 'rgba(12,10,26,.88)';
  mctx.fillRect(0, 0, mm.width, mm.height);
  for (let y = 0; y < h; y++) for (let x = 0; x < w; x++) {
    if (!solid[y][x]) { mctx.fillStyle = 'rgba(190,180,220,.28)'; mctx.fillRect(x * S, y * S, S, S); }
  }
  mctx.fillStyle = '#7dff9a';
  for (const t of exitTiles) mctx.fillRect(t.tx * S, t.ty * S, S, S);
  MapData.minimap = mm;
  MapData.minimapScale = S;
  return MapData;
}

function isSolidTile(tx, ty) {
  if (tx < 0 || ty < 0 || tx >= MapData.w || ty >= MapData.h) return true;
  return MapData.solid[ty][tx];
}
function isSolidAt(x, y) { return isSolidTile(Math.floor(x / TILE), Math.floor(y / TILE)); }
// 是否站在装饰地形上（冰面/水面）
function isDecorAt(x, y) {
  const tx = Math.floor(x / TILE), ty = Math.floor(y / TILE);
  if (tx < 0 || ty < 0 || tx >= MapData.w || ty >= MapData.h) return false;
  return MapData.decorGrid[ty][tx];
}

// 圆形 vs 瓦片碰撞：返回修正后的位置
function resolveCircle(x, y, r) {
  const tx0 = Math.floor((x - r) / TILE), tx1 = Math.floor((x + r) / TILE);
  const ty0 = Math.floor((y - r) / TILE), ty1 = Math.floor((y + r) / TILE);
  for (let ty = ty0; ty <= ty1; ty++) for (let tx = tx0; tx <= tx1; tx++) {
    if (!isSolidTile(tx, ty)) continue;
    const bx0 = tx * TILE, by0 = ty * TILE, bx1 = bx0 + TILE, by1 = by0 + TILE;
    const nx = Math.max(bx0, Math.min(x, bx1)), ny = Math.max(by0, Math.min(y, by1));
    const dx = x - nx, dy = y - ny;
    const d2 = dx * dx + dy * dy;
    if (d2 < r * r) {
      const d = Math.sqrt(d2) || 0.001;
      const push = r - d;
      x += (dx / d) * push; y += (dy / d) * push;
    }
  }
  return { x, y };
}

// 卡墙保险丝：实体中心陷进实心格时，弹回最近的地板格中心
function unstick(e) {
  if (!isSolidAt(e.x, e.y)) return;
  const tx = Math.floor(e.x / TILE), ty = Math.floor(e.y / TILE);
  for (let ring = 1; ring < 8; ring++) {
    for (let dy = -ring; dy <= ring; dy++) for (let dx = -ring; dx <= ring; dx++) {
      if (Math.max(Math.abs(dx), Math.abs(dy)) !== ring) continue;
      if (!isSolidTile(tx + dx, ty + dy)) {
        e.x = (tx + dx) * TILE + TILE / 2;
        e.y = (ty + dy) * TILE + TILE / 2;
        return;
      }
    }
  }
}

// 两点间是否无墙遮挡
function losClear(x1, y1, x2, y2) {
  const dx = x2 - x1, dy = y2 - y1;
  const dist = Math.hypot(dx, dy);
  const steps = Math.ceil(dist / (TILE * 0.4));
  for (let i = 1; i < steps; i++) {
    const t = i / steps;
    if (isSolidAt(x1 + dx * t, y1 + dy * t)) return false;
  }
  return true;
}

// 单条射线：返回撞墙前的最远距离（视野多边形用）
function castRay(x, y, angle, maxDist) {
  const step = 6;
  const cos = Math.cos(angle), sin = Math.sin(angle);
  let d = 0;
  while (d < maxDist) {
    d += step;
    if (isSolidAt(x + cos * d, y + sin * d)) return d - step * 0.4;
  }
  return maxDist;
}

// BFS 寻路（聪明怪物用）
function bfsPath(sx, sy, gx, gy) {
  const st = [Math.floor(sx / TILE), Math.floor(sy / TILE)];
  const gt = [Math.floor(gx / TILE), Math.floor(gy / TILE)];
  if (st[0] === gt[0] && st[1] === gt[1]) return [];
  const key = (x, y) => y * MapData.w + x;
  const prev = new Map();
  prev.set(key(st[0], st[1]), -1);
  const q = [st];
  let found = false;
  while (q.length && !found) {
    const [cx, cy] = q.shift();
    for (const [dx, dy] of [[1,0],[-1,0],[0,1],[0,-1]]) {
      const nx = cx + dx, ny = cy + dy;
      if (isSolidTile(nx, ny) || prev.has(key(nx, ny))) continue;
      prev.set(key(nx, ny), key(cx, cy));
      if (nx === gt[0] && ny === gt[1]) { found = true; break; }
      q.push([nx, ny]);
    }
  }
  if (!found) return [];
  const path = [];
  let k = key(gt[0], gt[1]);
  while (k !== -1 && k !== key(st[0], st[1])) {
    path.push({ x: (k % MapData.w) * TILE + TILE / 2, y: Math.floor(k / MapData.w) * TILE + TILE / 2 });
    k = prev.get(k);
  }
  return path.reverse();
}

// 按难度配置放置宝箱
function placeChests(diffCfg) {
  const chests = [];
  const shuffle = arr => { const a = arr.slice(); for (let i = a.length - 1; i > 0; i--) { const j = Math.floor(Math.random() * (i + 1)); [a[i], a[j]] = [a[j], a[i]]; } return a; };

  const golds = shuffle(MapData.goldSpots);
  for (let i = 0; i < diffCfg.chests.gold && i < golds.length; i++) chests.push({ ...golds[i], tier: 'gold' });
  for (let i = 0; i < diffCfg.chests.mystery && diffCfg.chests.gold + i < golds.length; i++) chests.push({ ...golds[diffCfg.chests.gold + i], tier: 'mystery' });

  const commons = shuffle(MapData.chestSpots);
  const used = new Set();
  const minDist = 4.5 * TILE;
  const pickSpread = (count, tier) => {
    let placed = 0;
    for (const s of commons) {
      if (placed >= count) break;
      if (used.has(s)) continue;
      if (chests.some(c => Math.hypot(c.x - s.x, c.y - s.y) < minDist)) continue;
      used.add(s); chests.push({ ...s, tier }); placed++;
    }
    for (const s of commons) {
      if (placed >= count) break;
      if (used.has(s)) continue;
      used.add(s); chests.push({ ...s, tier }); placed++;
    }
  };
  pickSpread(diffCfg.chests.silver, 'silver');
  pickSpread(diffCfg.chests.wood, 'wood');
  return chests;
}
